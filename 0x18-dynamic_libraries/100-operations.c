#include <stdio.h>
#include "main.h"

/**
 * add - function that performs addition
 * @a: first number
 * @b: second number
 * Return: sum
 */
int add(int a, int b)
{
return a + b;
}

/**
* sub - function that performs subtraction
* @a: first number
* @b: second number
* Return: difference
*/
int sub(int a, int b)
{
return a - b;
}

/**
* mul - function that performs multiplication
* @a: first number
* @b: second number
* Return: product
*/
int mul(int a, int b)
{
return a * b;
}

/**
* div - function that performsdivision
* @a: first number
* @b: second number
* Return: quotient
*/
int div(int a, int b)
{
return a / b;
}

/**
* add - function that performs modulus
* @a: first number
* @b: second number
* Return: remainder
*/
int mod(int a, int b)
{
return a % b;
}
